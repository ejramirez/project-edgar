    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            JFileChooser fc;
            fc = new JFileChooser();
            FileNameExtensionFilter filter = new FileNameExtensionFilter("accdb files (.accdb)","accdb");
            fc.setFileFilter(filter);
            File f = new File(System.getProperty("user.dir"));

            fc.setCurrentDirectory(f);
            fc.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
            fc.showOpenDialog(null);
            fc.setDialogTitle("Choose Database");

            File selFile = fc.getSelectedFile();
            System.out.println(selFile);
            Config.setProperty("CurrentDatabasePath", selFile.toString());
            System.out.println("Path Set.");
            jTextField1.setText(selFile.toString());
        } catch (IOException ex) {
            Logger.getLogger(SettingsFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

